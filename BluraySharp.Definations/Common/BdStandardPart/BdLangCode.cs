		public static implicit operator BdLang(BdLangCode langCode)
		{
			string tCode = langCode.Value.ToLower(System.Globalization.CultureInfo.InvariantCulture);
			return BdLangCode.langCodeTable.First(x => x.Value.Equals(tCode)).Key;
		}

		public static implicit operator string(BdLangCode langCode)
		{
			return langCode.Value;
		}

		public static implicit operator BdLangCode(string langCodeString)
		{
			return new BdLangCode() { Value = langCodeString };
		}

		public static implicit operator BdLangCode(BdLang langValue)
		{
			return new BdLangCode() { Value = BdLangCode.langCodeTable[langValue] };
		}

		public override string ToString()
		{
			return this.Value;
